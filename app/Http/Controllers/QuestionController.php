     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'question' => 'required',
        ]);

        $question = new Question();
        $question->user_id = Auth::user()->id;
        $question->question = $request->question;
        $question->answer = ("");
        $question->save();

        // Redirecionar o usuário para a página de perguntas ou exibir uma mensagem de sucesso

        return redirect()->route('questions.index')->with('success', 'Sua pergunta foi enviada com sucesso.');
    }

    public function answer(Request $request, Question $question)
{
    $request->validate([
        'answer' => 'required',
    ]);

    $question->answer = $request->answer;
    $question->user_id = Auth::user()->id;
    $question->save();

    return redirect()->route('questions.index')->with('success', 'Resposta enviada com sucesso.');
}


